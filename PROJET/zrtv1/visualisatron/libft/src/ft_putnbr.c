#include "libft.h"

void	ft_putnbr(int n)
{
	long	nb;

	nb = n;
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr((int)(nb / 10));
		ft_putnbr((int)(nb % 10));
	}
	else
		ft_putchar((char)nb + '0');
}
