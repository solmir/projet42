#include "libft.h"

void	ft_ptrswap(void *p1, void *p2)
{
	int		tmp;
	int		*i1;
	int		*i2;

	i1 = (int *)p1;
	i2 = (int *)p2;
	tmp = *i1;
	*i1 = *i2;
	*i2 = tmp;
}
